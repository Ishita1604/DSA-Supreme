// For Loop:
// To repeat code "n" number of times.
// Instead of writing same code "n" times.
// Syntax: for (initialisation; condition; update)
// We use the "counter" variable to maintain number of times to run the code.

#include <iostream>
#include <string>
using namespace std;

int main()
{
    for (int i = 0; i < 5; i++)
    {
        cout << "C++ is awesome!" << endl;
    }

    return 0;
}